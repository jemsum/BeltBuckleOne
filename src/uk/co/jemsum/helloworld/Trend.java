package uk.co.jemsum.helloworld;

public class Trend
{
    String events; 
    String name; 
    String promoted_content; 
    String query;
    String url;
    
    
    
	public String getEvents()
	{
		return events;
	}
	public void setEvents(String events)
	{
		this.events = events;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getPromoted_content()
	{
		return promoted_content;
	}
	public void setPromoted_content(String promoted_content)
	{
		this.promoted_content = promoted_content;
	}
	public String getQuery()
	{
		return query;
	}
	public void setQuery(String query)
	{
		this.query = query;
	}
	public String getUrl()
	{
		return url;
	}
	public void setUrl(String url)
	{
		this.url = url;
	}
    
    
    
    
}
